const clamp = (value: number, min: number, max: number) => Math.max(min, Math.min(max, value));

const hslToHex = (hue: number, saturation: number, lightness: number) => {
  const h = ((hue % 360) + 360) % 360;
  const s = clamp(saturation, 0, 100) / 100;
  const l = clamp(lightness, 0, 100) / 100;

  const chroma = (1 - Math.abs(2 * l - 1)) * s;
  const huePrime = h / 60;
  const x = chroma * (1 - Math.abs((huePrime % 2) - 1));

  let r = 0;
  let g = 0;
  let b = 0;

  if (huePrime >= 0 && huePrime < 1) {
    r = chroma;
    g = x;
  } else if (huePrime >= 1 && huePrime < 2) {
    r = x;
    g = chroma;
  } else if (huePrime >= 2 && huePrime < 3) {
    g = chroma;
    b = x;
  } else if (huePrime >= 3 && huePrime < 4) {
    g = x;
    b = chroma;
  } else if (huePrime >= 4 && huePrime < 5) {
    r = x;
    b = chroma;
  } else {
    r = chroma;
    b = x;
  }

  const match = l - chroma / 2;
  const toHex = (channel: number) =>
    Math.round((channel + match) * 255)
      .toString(16)
      .padStart(2, "0");

  return `#${toHex(r)}${toHex(g)}${toHex(b)}`;
};

const getExtraRegularThemeName = (index: number) => {
  const prefix = EXTRA_REGULAR_PREFIXES[index % EXTRA_REGULAR_PREFIXES.length];
  const suffix = EXTRA_REGULAR_SUFFIXES[Math.floor(index / EXTRA_REGULAR_PREFIXES.length) % EXTRA_REGULAR_SUFFIXES.length];
  return `${prefix}_${suffix}_${String(index + 1).padStart(3, "0")}`;
};

const generatedRegularThemes: MonkeytypeTheme[] = Array.from({ length: EXTRA_REGULAR_THEME_COUNT }, (_, index) => {
  const hue = (index * GOLDEN_ANGLE + (index % 9) * 12) % 360;
  const family = index % 8;
  const bgLightness = family < 4 ? 8 + family * 2 : 14 + (family - 4) * 4;
  const textLightness = bgLightness < 22 ? 89 : 18;

  const mainHue = (hue + 18 + family * 11) % 360;
  const caretHue = (mainHue + 34) % 360;
  const errorHue = (mainHue + 184) % 360;
  const accentHue = (mainHue + 142) % 360;

  return {
    name: getExtraRegularThemeName(index),
    bg: hslToHex(hue, 20 + family * 5, bgLightness),
    main: hslToHex(mainHue, 66 + (family % 3) * 8, family < 4 ? 62 : 46),
    caret: hslToHex(caretHue, 72, family < 4 ? 70 : 54),
    sub: hslToHex((hue + 20) % 360, 18 + family * 3, family < 4 ? 48 : 38),
    subAlt: hslToHex((hue + 8) % 360, 16 + family * 2, bgLightness + (family < 4 ? 6 : 10)),
    text: hslToHex((hue + 6) % 360, 24, textLightness),
    error: hslToHex(errorHue, 70, family < 4 ? 62 : 48),
    errorExtra: hslToHex(errorHue, 50, family < 4 ? 36 : 30),
    colorfulError: hslToHex(accentHue, 68, family < 4 ? 64 : 46),
    colorfulErrorExtra: hslToHex(accentHue, 46, family < 4 ? 34 : 28),
  };
});

export const monkeytypeThemes: MonkeytypeTheme[] = [...baseMonkeytypeThemes, ...generatedRegularThemes];
